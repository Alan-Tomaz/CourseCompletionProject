    private void consultar() {
        String sql = "select * from tb_users where iduser = ?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, idText.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField1.setText(rs.getString(4));
                jComboBox1.setSelectedItem(rs.getString(5));
            } else {
                JOptionPane.showMessageDialog(null, "Usuário não cadastrado");
                jTextField2.setText(null);
                jTextField3.setText(null);
                jTextField1.setText(null);
                jComboBox1.setSelectedItem("user");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void adicionar() {
        String sql = "insert into tb_users(iduser, usuario, login, senha, perfil) values(?,?,?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, idText.getText());
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jTextField3.getText());
            pst.setString(4, jTextField1.getText());
            pst.setString(5, jComboBox1.getSelectedItem().toString());

            if (idText.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios!");
            } else {

                int adicionado = pst.executeUpdate();
                System.out.println(adicionado);
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Usuário adicionado com sucesso");
                    idText.setText(null);
                    jTextField2.setText(null);
                    jTextField3.setText(null);
                    jTextField1.setText(null);
                    jComboBox1.setSelectedItem("user");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

